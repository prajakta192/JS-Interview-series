// JS DATA TYPE
//Primitive type

var dataType;     //1 => undefined  typeof undefined
dataType = 'Bob' // 2 => string     typeof string
dataType = 123  //  3 => number     typeof number
dataType = true //  4 => boolean    typeof boolean
dataType = null //  5 => null       typeof object
dataType = Symbol('hi') // symbol  unique value that can't be changed  typeof symbol
dataType = BigInt(75757575757575757575757575756) // BigInt  // typeof BigInt

console.log(dataType)
